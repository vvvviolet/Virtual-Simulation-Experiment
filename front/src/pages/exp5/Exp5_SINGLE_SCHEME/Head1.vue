    <p class="content">    2.3 单方案经济评价具有两种评价指标：分别是静态评价指标和动态评价指标；静态评价指标指的是不考虑资金时间价值，从会计收益视角计算评价指标，主要指标是总投资收益率、资本金净利润率、投资回收期；动态评价指标指的是在考虑了资金时间价值以及项目在整个寿命期内的收支情况全部数据后，经计算得到的评价指标，主要包括净现值（NPV）、内部收益率（IRR）、动态投资回收期（DPP）等指标。</p>
